import React , {useState} from 'react';
interface ITableData 
  {
    kjoretoyId:{kjennemerke:string, understellsnummer:string}, 
    kodeBeskrivelse:string, 
    merke:[{merke:string}],
    handelsbetegnelse:string,
    kontrollfrist:string,
    sistGodkjent:string
  }

function App() {
   const [number , setNumber] = useState("");
   const [tableData , setTableData] = useState({} as ITableData);

   const {kjoretoyId , merke, handelsbetegnelse, kontrollfrist, sistGodkjent} = tableData;
  const getVehivleInfo = (number:any) => {
    fetch(
      `/api/info/${number}`,     
    )
      .then((res) => res.json())
      .then((data) => {
        setTableData(data);
        console.log(tableData);
      })
      .catch((err) => console.log(err));
    }

const handleChange = (e:any) => {
  setNumber(e.target.value);
}

  return (
    <div className="App">
      <div >
        <h2>Enter the vehicle registration number</h2>
        <div className='inputDiv'>
          <input type="text" value={number} onChange={handleChange} />
          <button onClick={()=>{
            getVehivleInfo(number);
          }}>Submit</button>
      </div>
      {tableData.kjoretoyId &&
      <table>
        <tr>
          <th>Kjennemerke</th>
          <td>{kjoretoyId?.kjennemerke}</td>
        </tr>
        <tr>
          <th>Understellsnummer</th>
          <td>{kjoretoyId.understellsnummer}</td>
        </tr>
        <tr>
          <th>Merke</th>
          <td>{merke[0].merke}</td>
        </tr>
        <tr>
          <th>Handelsbetegnelse</th>
          <td>{handelsbetegnelse}</td>
        </tr>
        <tr>
          {/* <th>Forstegangsregistrering</th>
          <td>{registrertForstegangNorgeDato}</td> */}
        </tr>
        <tr>
          <th>SistGodkjent</th>
          <td>{sistGodkjent}</td>
        </tr>
        <tr>
          <th>Kontrollfrist</th>
          <td>{kontrollfrist}</td>
        </tr>
      </table>}
      </div>
    </div>
  );
}

export default App;




